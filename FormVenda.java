    public void fimVenda(Atendente atend, Cliente cli, List<Produto> produtos, float precoTotal){
        Compra novaCompra = new Compra();
        novaCompra.setIdCompra(quantidadeCompra + 1);
        novaCompra.setCliente(cli);
        novaCompra.setValorTotal(precoTotal);
        novaCompra.setDataCompra(LocalDate.now());
        novaCompra.setAtendente(atend);
        novaCompra.setProdutos(produtos);
        atend.setTotalVendas(atend.getTotalVendas() + 1);
        atend.setValorTotalVendas(FormRelProdutos.geraRelProd().getPrecoTotal());
        
        JOptionPane.showMessageDialog(
                null,
                "Venda concluida com sucesso! verifique os dados",
                "Sistema de venda",
                JOptionPane.INFORMATION_MESSAGE
        );
        
        cxNomeAtend.setText(atend.getNome());
        cxNomeCliente.setText(cli.getNome());
        cxIdCompra.setText(Integer.toString(novaCompra.getIdCompra()));
        cxValorTotal.setText(Float.toString(novaCompra.getValorTotal()));
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        cxDataCompra.setText(novaCompra.getDataCompra().format(formato));
        
        quantidadeCompra++;
    }
    
    public void inicioVenda(){
        String cpf_a = JOptionPane.showInputDialog(
                null,
                "Informe o CPF do atendente : ",
                "Sistema de venda",
                JOptionPane.INFORMATION_MESSAGE
        );
        
        for (Atendente a : gp.getBdAtend()){
            if (cpf_a.equals(a.getCpf())){
                Atendente atendVenda = a;
                
                String cpf_c = JOptionPane.showInputDialog(
                        null,
                        "Informe o CPF do Cliente : ",
                        "Sistema de venda",
                        JOptionPane.INFORMATION_MESSAGE
                );
                
                for (Cliente c : gp.getBdCli()){
                    if (cpf_c.equals(c.getCpf())){
                        
                        Cliente clienteVenda = c;
                        
                        JOptionPane.showMessageDialog(
                                null,
                                "Atendente e cliente encontrados!",
                                "Sistema de venda",
                                JOptionPane.INFORMATION_MESSAGE
                        );
                        
                        FormRelProdutos relProd = FormRelProdutos.geraRelProd();
                        relProd.setDadosVenda(atendVenda, clienteVenda, this);
                        relProd.setVisible(true);
                        this.setVisible(false);
                        
                        JOptionPane.showMessageDialog(
                                null,
                                "Clique em um produto para adicionÃ¡-lo!",
                                "Sistema de venda",
                                JOptionPane.INFORMATION_MESSAGE
                        );
                        
                    }// fim ifCliente
                    else {
                        JOptionPane.showMessageDialog(
                                null,
                                "ERRO : NÃ£o existe cliente com esse CPF!",
                                "Sistema de vendas",
                                JOptionPane.ERROR_MESSAGE
                        );
                    }
                }// fim ForCliente
            } else {
                JOptionPane.showMessageDialog(
                        null,
                        "ERRO : NÃ£o existe atendente com esse CPF!",
                        "Sistema de vendas",
                        JOptionPane.ERROR_MESSAGE
                );
            } // fim ifAtendente
        }// fim ForAtendente
    }// fim inicioVenda
    
